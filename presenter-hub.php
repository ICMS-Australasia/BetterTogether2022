<h2>ACCESSIBLE PRESENTATIONS</h2>
<p>We ask that you make sure to consider that your pre-recorded presentations are accessible, we have provided some helpful tips and resources below to guide you in preparing accessible presentations.</p>
<ul>
    <li><b>Language:</b> use plain language and avoid or explain jargon or technical terms</li>
    <li><b>Fonts:</b> use large (at least 24 point), simple, san serif fonts (e.g., Arial, Verdana, Helvetica) </li>
    <li><b>Colours:</b> Use high contrast colours. Do not use colour as the only method for distinguishing information.</li>
    <li><b>Subtitles:</b> add subtitles to your recording. It’s easy and we have lots of instructions and guidance on how to do this. </li>
    <li><b>Outline:</b> For interactive sessions, consider displaying an outline at the beginning of your talk, so that audience members know what to expect during the presentation.</li>
    <li><b>Content of slides:</b> Minimize the amount of text on slides, limit visuals or use simple visuals. Describe images, graphs and charts and read out important quotes or definitions.</li>
    <li><b>Advancing slides:</b> keep animations and transitions between slides simple and not distracting, pause when advancing a slide to let people read it before saying anything.</li>
</ul>

