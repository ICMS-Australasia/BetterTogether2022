<p>This webpage is a resource for all presenters, providing guidelines, instructions as well as tips and tricks for preparing for your virtual presentation.</p>

<h2>KEY POINTS</h2>
<ul>
    <li><b>Accessibility:</b> our audience includes people with disability, family members and carers. We encourage you to think about accessibility in your presentation. Use plain language, think carefully about jargon terms, and use large font and simple graphs. And add subtitles to your presentation video; we promise it’s easier than you think! </li>
    <li><b>International audience:</b> our audience includes people from across the world, with many languages. Make sure your presentation is clear and speak slowly and clearly so the international audience can understand.</li>
    <li><b>Interactivity:</b> this is a fully virtual conference. Try to make your session as engaging and fun as you can.</li>
    <li><b>Take home message:</b> we encourage you to include a final slide with key take home messages. Consider what your research means for people with disability and their families as well as clinicians</li>
</ul>

