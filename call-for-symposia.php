<h3>Focused Symposia</h3>

<p><b>Definition:</b> Symposia are sessions that examine important issues from a variety of perspectives. During these sessions, alternative solutions, interpretations and points of view on a body of knowledge within the field are presented. Symposia that include presenters from different disciplines, career stage (early career researchers encouraged) and include cross-cutting themes relevant to the overall meeting, as well as the program tracks are strongly encouraged. Symposia that include presenters from different countries in high and low resource settings are encouraged to highlight the variability in practice or research knowledge in varying contexts. A symposium typically includes one chair, 3-4 presenters and/or discussants. The chair can also serve as one of the presenters and/or discussant. If two symposia are proposed on a similar topic, the conference committee may encourage joining of symposia.</p>
<p><b>Submission Format:</b> Chair (required); Presenter (required); Discussant (optional); Author (optional) Minimum of 3 participants. Include a session title, description, and a topic outline with individual presentation titles/descriptions.</p>
<p><b>Presentation Format:</b> Total time allocation for session is 90 minutes. Introduction of each presenter and overall topic by chair (10 minutes); each presenter discusses their position (10 minutes); presenters continue discussion; chair will prompt discussion between presenters. Audience Q&A is encouraged for the last 15 minutes of the presentation.</p>
<h3>Debate/Current Controversies</h3>
<p><b>Definition:</b> Expert panellists with different opinions have the opportunity to engage in a debate to provide different views (preferably opposite from each other) on a particular issue. The session will be heavily moderated by a Chair/Moderator.  Debates/Controversies that include presenters from different perspectives/ different countries and or continents are encouraged to highlight the variability in practice or research knowledge in varying contexts.</p>
<p><b>Submission Format:</b> Chair/Moderator (required); Presenters (minimum of 2; maximum of 4). Minimum of 3 participants. Include a session title, description, and a topic outline with individual presentation titles/descriptions.</p>
<p><b>Presentation Format:</b> Total time allocation for session is 90 minutes. Introduction by moderator (5 minutes); each panellist defends their position (5-10 minutes); panellists get into debate and provide supplementary points (5 minutes each); moderator will ask different questions to challenge each position; maximum amount of time for panellist presentations 60 minutes; 30 minutes to be reserved for discussion. Q&A for five minutes is encouraged after every section of the debate.</p>
<p>Submissions are welcomed over a wide range of conditions and themes;</p>

