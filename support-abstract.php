 <p>The Better Together 2022 Conference Committee aims to support a high quality experience for all participants. We ask our presenters to consider how they will present their work so that it is equitable, engaging, and accessible for all attendees- no matter what their background, nationality, or method of attending. Here are tips for making your presentation 1. Engaging and 2. Accessible.</p>

 <h2>1.Tips and considerations for creating an engaging virtual presentation</h2>
 <ul>
   <li>Will you want to use chat rooms, break out rooms, or polling questions throughout your session?</li>
   <ul>
     <li>In the chat room: Online moderators can chat along and answer questions to the virtual audience during the session, and can consider summarising key themes of questions /discussion points to the ‘in room’ audience.</li>
     <li>Polls: Adding questions throughout the session that people can answer or vote on is a great way to get to know more about your audience / their understanding on a topic or opinions.</li>
     <li>Breakout rooms: In the same way you can ‘break out’ into small groups in person, you can have ‘virtual break out’ rooms. These usually work best with small groups of 3-5 people, with everyone prepared to have their videos and microphones on within the smaller groups. The presenters or moderators can ‘virtually’ enter in and out of these groups to join in the discussions online.</li>
     <li>‘Chunking’ of information within workshops: Long virtual talks can be fatiguing, but if you can break longer talks up into smaller ‘chunks’ of 7 minute talks mixed with activity then it can help break up the mental load</li>
   </ul>
   <li>Avoid in room discussions that exclude the virtual audience: all questions and answer time will go through a microphone for our virtual audience</li>
   <li>Have someone monitoring the online discussion during the session</li>
 </ul>

 <h2>2. Tips and considerations for creating an accessible presentation</h2>
<ul>
  <li>Use large (at least 24 point), simple, san serif fonts (e.g., Arial, Verdana, Helvetica) that can be easily read by most individuals.</li>
  <li>Use high contrast colours to assist audience members with low vision or colour blindness.</li>
  <li>Do not use colour as the only method for distinguishing information.</li>
  <li>Minimize the amount of text on slides.</li>
  <li>When you advance a slide, pause to let people read it before saying anything. This will allow people who are hearing impaired and everyone else in the audience to read the slide before you start talking.</li>
  <li>Animations and transitions between slides should be simple and not distracting.</li>
  <li>For workshops, symposia’s, breakfast sessions- consider displaying an outline at the beginning of your talk, so that audience members know what to expect during the presentation.</li>
  <li>For particular quotes or definitions, consider reading the text on the slide to make sure people who are vision impaired know what is on the slide.</li>
  <li>Limit the number of visuals on slides. Images that are used should be described, graphs and charts should be described and summarized.</li>
  <li>Avoid presenting images of complex charts or tables. Make graphics as simple as possible or use symbols to draw attention to specific facts.</li>
  <li>Have you considered including closed captioning of your recording?</li>
</ul>

