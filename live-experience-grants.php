<h2>Grants for people with disability and their supporters to attend the conference.</h2>
<p>The Better Together Conference Committee is pleased to offer 50 Scholarships for people with disability and their family members or caregivers that will cover the cost of conference registration. We are committed to delivering a conference program that is relevant to researchers, practitioners and people with disability and their supporters.</p>
<p>This conference will bring together researchers, practitioners and people with lived experience of disability to share knowledge about childhood onset disability. The conference is a fully virtual event that will run over four days with over 100 sessions. Keynote speakers and presenters will share the latest information on a range of topics including new technology, robotics, participation in the community, orthopaedics, communication, early intervention, pain, sleep, mental health, family wellbeing and more.</p>
<p>Scholarships will cover the cost of registration for four full days, and will also allow access to all program content for 3 months afterwards, access to the virtual trade displays, networking, meet the speakers and other social events. Apply now!</p>

<a href="https://icmsaust.eventsair.com/ausacpdmiaacd-2022---childhood-disabilities/le-scholarship" target="_blank" class="button d-block text-center">Apply at this link</a>

<p>This Scholarship opportunity is available to people with disability, their family and supporters. You will be asked to outline your lived experience and why you are interested in attending the Better Together Conference in the application form. Scholarships will be offered to the first 50 applicants who complete the online application form with these relevant details.</p>

