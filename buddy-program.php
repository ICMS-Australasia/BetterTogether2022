<h2>Join The Buddy Program</h2>
<p>The Australasian Academy of Cerebral Palsy and Developmental Medicine (AusACPDM), as host and part of the International Alliance of Academies of Childhood Disability (IAACD), Better Together 2022 conference aims to share information from: </p>
<ul>
    <li>A clinical science-based perspective</li>
    <li>Lived experience</li>
    <li>Consumer driven research</li>
</ul>

<p>These areas are not independent, as no matter which area we relate to the most, we have an influence on each area from our own experiences. A valuable part of the conference is the opportunity to develop contacts, share this experience and learn from each other. </p>

<p>For those of you with lived experience, who may be new to the world of conferences, we are offering an opportunity to virtually link with each other and members of AusACPDM, prior to the conference, to get a head start on establishing contacts. We also plan to offer opportunity during the conference to catch up. </p>

