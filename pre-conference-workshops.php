      <p>Improving the physical and mental health is an imperative for adults with cerebral palsy (CP). Evidence is emerging of poor health resulting in reduced quality of life and shortened lifespan. Whilst comprehensive services are available for children through paediatric networks, there is a clinical and research void after transition and into and throughout adulthood. Our vision is a world where people with CP receive the best care throughout their lives and are thus enabled to participate more fully in society.</p>
      <p>This workshop by an international panel from the United States, Canada, Sweden and Australia will focus on new and challenging issues in adults with CP and how they might be addressed. Current knowledge about the physical and mental health status of adults will be presented, including healthcare services research in Europe, health status and unmet rehabilitation needs in Sweden, and participation-focused interventions to increase physical activity across Australia.</p>
