<p>On behalf of the Australasian Academy of Cerebral Palsy and Developmental Medicine (AusACPDM) and the International Alliance of Academies of Childhood Disability (IAACD), we invite and welcome you to the Joint 11th AusACPDM biennial, and 3rd IAACD triennial international conference in Melbourne, Australia 1-5th March 2022.</p>
<p>The Conference theme Better Together embodies the values and visions of our Academies to come together to ensure rapid dissemination and global impact through partnership. We invite you to connect, communicate and collaborate, before, during and after the conference. Our scientific program is being developed in partnership with professionals from many disciplines, across all regions of the world, as well as community partners - including families and young people with child-onset disability. Our scientific program offers a broad range of submissions of cutting-edge international research, best practice interventions, knowledge translation and inclusion, from both high and low resource settings. We aim to be electronically and physically accessible to ensure broad reach to educate and extend our knowledge and skills.</p>
<p>Melbourne, our host city in Australia, is known as a knowledge, research and innovation capital, hosting multiple world-class medical and biotechnology research centres and institutions. This makes it the perfect place to bring international delegates together to advance knowledge and practice for the benefit of those living with child-onset disability. With a temperate climate and ease of access to beaches, the Great Ocean Road and much of Australia’s amazing wildlife, visiting Melbourne for the conference might be just the beginning of your adventure.</p>
<p>Our experiences with COVID-19 this year have increased our awareness that we live with uncertainty and that the future of international travel is hoped, and planned for, but currently unknown. We want to assure all our potential speakers, sponsors and attendees that the Conference Committee is planning for multiple contingencies to ensure a high-quality scientific program. At a minimum, this will be a hybrid conference so that anyone can attend on-line as well as in person. We are developing a range of activities and resources that aim to support high quality experiences for all. We will continue to update you as the program and our activities evolve. Please put the Conference website on your ‘quick-links’ http://www.bettertogether2022.org/  and sign up for our e-zines to keep informed.</p>
<p>We are Better Together, and we look forward to working, learning and collaborating with you going forward.</p>

