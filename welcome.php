<p>On behalf of the Australasian Academy of Cerebral Palsy and Developmental Medicine (AusACPDM) and the International Alliance of Academies of Childhood Disability (IAACD), we invite and welcome you to the Joint 11th AusACPDM biennial, and 3rd IAACD triennial international conference hosted from Melbourne, Australia 1-5th March 2022.</p>
<p>The Conference theme Better Together embodies the values and visions of our Academies to come together to ensure rapid dissemination and global impact through partnership. We invite you to connect, communicate and collaborate, before, during and after the conference. Our scientific program is being developed in partnership with professionals from many disciplines, across all regions of the world, as well as community partners - including families and young people with child-onset disability. Our scientific program offers a broad range of submissions of cutting-edge international research, best practice interventions, knowledge translation and inclusion, from both high and low resource settings.</p>
<p>We had anticipated being able to welcome you in person to Melbourne as our host city and to show case our Australian way of life, as well as the knowledge and innovations evident in our city. Sadly, the COVID-19 situation continues to bring great uncertainty regarding the possibility of travel both within Australia and internationally.</p>
<p>Because of this uncertainty, the Conference Committee, in collaboration with the Board of the AusACPDM and the Steering Group of the IAACD, have recently decided that the conference will proceed in a fully virtual format. This will mean some re-imagining of the program – and we are using the collective talents of our colleagues across the region and the world to bring to you an innovative, accessible and exciting program.</p>
<p>We continue to develop a range of activities and resources that aim to support high quality experiences for all. You will see changes on the website as we firm up the new program and we will continue to update you via e-zines and web notices as the program and our activities develop. Please put the Conference website on your ‘quick-links’ <a href="http://www.bettertogether2022.org/">http://www.bettertogether2022.org/</a> and sign up for our e-zines to keep informed.</p>
<p>We are Better Together, and we look forward to working, learning and collaborating with you going forward.</p>

