<p>The Board of Directors of the Australasian Academy of Cerebral Palsy and Developmental Medicine (AusACPDM) is committed to making the Conference accessible to persons from low resource communities or workplaces, or where attendance at the Conference would not be possible without additional funding support.</p>
<p>This will be a hybrid conference. For planning purposes, it is requested that the applicant select the type of scholarship they are applying for, that is, <b>in person or virtual</b>. Given the ever-changing requirements related to travel due to COVID-19, scholarships for International applicants will be virtual only. If an in-person scholarship is awarded to an Australasian applicant and travel is not permitted, then it will be converted to a virtual scholarship. If a virtual scholarship is awarded but in-person is decided by the individual, then the value of the virtual scholarship can be used to support attendance. Once the scholarships have been awarded, there will be no opportunity to change the monetary value of the scholarship. Refer to <a href="http://www.health.gov.au" target="_blank">www.health.gov.au</a>; <a href="http://www.interstatequarantine.org.au" target="_blank">www.interstatequarantine.org.au</a> for updates on Covid-19.</p>
<p>The AusACPDM will be offering a number of different streams of Scholarships in 2022:</p>
<ul>
  <li>Australasian (Australia/NZ/Pacifica) attendees (in person or virtual)</li>
  <li>International (Non-OECD countries) attendees (virtual only)</li>
  <li>2 x Work site scholarships for work groups of attendees from low resource settings (virtual only)</li>
</ul>

