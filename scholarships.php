<h2>Scholarship Inclusions</h2>
<p>The scholarship gives you the opportunity to network virtually or in person with world class industry leaders and stay up to date with the latest clinical care, research and evidence-based practice.</p>
<p>All scholarships (virtual and in-person) include:</p>
<ul>
  <li>Complimentary registration to the conference (excluding pre-conference workshops, breakfast seminars and conference social events not included in standard registration costs). Work site scholarships will include a single virtual link for the duration of the conference, with the intention that the work site recipient group are able to engage with conference content as a group to enable discussion and enhance the virtual conference experience.</li>
  <li>Complimentary ticket to attend the International Affairs Luncheon</li>
</ul>
<p>Applicants applying for in-person attendance may also apply to receive funding for reimbursement of travel and/or accommodation, depending on location of the applicant and as determined by the Scholarship committee.</p>
<hr class="my-4">
<h2>International Affairs Luncheon</h2>
<p>All scholarship recipients attending in person and virtually are required to attend the International Affairs luncheon.</p>
<p>Five of the scholarship winners will be invited by the committee to speak at this event.</p>
<hr class="my-4">
<h2>Eligibility Criteria</h2>
<p>To be eligible to receive a Conference scholarship, candidates must:</p>
<ul>
  <li>Demonstrate proof of current <a href="https://www.ausacpdm.org.au/become-a-member/" target="_blank">AusACPDM membership</a> or <a href="https://iaacd.net/about-iaacd/member-academies/" target="_blank">membership of a Full or Provisional Academy Member of the IAACD</a></li>
  <li>Provide a written statement from their institution stating that other possible sources of funding have been applied for and institutional funding is not available;</li>
  <li>Ensure the Recommendation Form is submitted by their employer, supervisor or academic instructor;</li>
  <li>Provide proof of country of residence;</li>
  <li>Provide a recent photo with the application. Successful applicant photos will be used to assist with mentor recognition;</li>
  <li>Complete the self-application form (with signature) – nominations by other parties will not be accepted;</li>
  <li>Not be a recipient of other AusACPDM funding e.g PhD platform scholarship, invited speaker to the Conference;</li>
  <li>Agree and provide signed consent to have their name and photo published as a scholarship recipient in the Conference app and on the AusACPDM website;</li>
  <li>Agree to provide a written report (300 words) within 2 months of the Conference on how their attendance at the Conference has assisted in improving their knowledge, skills and service delivery. NB: Previous successful applicants cannot apply for further scholarship funding for the next 2 AusACPDM Conferences (i.e. 4 years).</li>
</ul>

<hr class="my-4">
<h2>Prioritisation</h2>
<p>Priority is given to those applicants:</p>
<ul>
  <li>Who can demonstrate in their application how participation will enhance their own knowledge, skills and service delivery;</li>
  <li>Who have a sound plan for disseminating and implementing the acquired knowledge and skills with colleagues in their community or country;</li>
  <li>Who’s clinical and research clients might benefit the most from the new knowledge and skills of the applicant and their colleagues.</li>
</ul>

<hr class="my-4">
<h2>Selection Criteria</h2>
<p>Applications received by 30 April 2021 will be checked for completeness. All complete applications will be forwarded to the International Affairs Committee for review.</p>
<p>Applications will be reviewed, discussed and scored by the Committee members, with a final decision made by the end of August. Applicants will be notified by 31 August 2021.</p>

