 <h5 class="teal mt-4"><em>This pre-conference session will run from 8:30am – 10:00am (AEDT) and is available for virtual registration only.</em></h5>
 <p><b>Implementation, challenges and impact of four parent-delivered training programmes for children with developmental disabilities in lower and middle income countries (LMIC)</b></p>
 <p><em>Hans Forssberg, Gillian Saloojee, Rachel Lassman, Katherine Benfer, Pamela Dixon</em></p>
 <p>This symposium focuses on the implementation of four parent-led training programmes for children with developmental disabilities living in resource-constrained settings. Presenters will describe the training, implementation and outcomes of each programme. This will be followed by a discussion how challenges common to all programmes were addressed.</p>

