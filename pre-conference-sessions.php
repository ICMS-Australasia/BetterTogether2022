<h3 class="teal">¾ Day Pre-Conference Sessions</h3>

<h5 class="teal mt-4"><em>This pre-conference session will run from 8:30am – 2:30pm (AEDT) and is available for both in-person and virtual registration.</em></h5>
 <p><b>Let’s get strategic about physical activity participation for people with disability across the lifespan</b></p>
 <p><em>Sian Williams, Gaela Kilgour, Olaf Verschuren, Sarah Reedman, Nora Shields, and Jan Willem Gorter</em></p>
 <p>This symposium will focus on a lifespan and all abilities approach to promote 24 hour activity guidelines including sleep in people with disability. Panellists will discuss health and wellbeing benefits of physical activity, behaviour change as relevant for clinicians, and participation as a key to achieve sustained lifelong benefits.</p>

<hr>

 <h5 class="teal mt-4"><em>This pre-conference session will run from 8:30am – 2:30pm (AEDT) and is available for both in-person and virtual registration.</em></h5>
 <p><b>Preclinical studies, neuroprotection, prevention and neural repair</b></p>
 <p><em>MinYoung Kim, Suzie Miller, Iona Novak, Kylie Crompton, So Yoon Ah, Atul Malhorta, Madison Paton, Megan Finch Edmonson,</em></p>
 <p>Cell-based therapies have neuroprotective and neuroregenerative potential for perinatal brain injury. This symposium brings together international experts to provide a status update on clinical trials for cell therapies, and how neuroprotective efficacy might be enhanced. We will also examine what is on the horizon for novel therapies particularly aimed at the preterm brain</p>

<hr>

 <h5 class="teal mt-4"><em>This pre-conference session will run from 8:30am – 2:30pm (AEDT) and is available for both in-person and hybrid registration.</em></h5>
 <p><b>Early Detection of cerebral palsy - experiences and challenges</b></p>
 <p><em>Cath Morgan, Iona Novak, Atul Malhotra, Esther Tantsis, Dr Samanmali Sumanasena, Sian Williams</em></p>
 <p>International Guidelines on early detection of cerebral palsy have been available for the past 5 years and have seen changes in practice globally. In this workshop, global experiences of translation will be discussed. Examples of clinical conundrums and implementation challenges faced by clinicians in the early diagnosis of CP will be presented.</p>

<hr>

 <h5 class="teal mt-4"><em>This pre-conference session will run from 8:30am – 2:30pm (AEDT) and is available for virtual registration only.</em></h5>
 <p><b>Selective Dorsal Rhizotomy experiences around the world</b></p>
 <p><em>Nelleke Langerak, Tom Novachek, Nico Enslin, Kristian Aquilina, Simon Paget, Marcie Ward</em></p>
 <p>This will be a global, multidisciplinary course reviewing selective dorsal rhizotomy (SDR) including: spasticity management overview, SDR techniques, practices and experiences, post-operative management, policies and services, short-and long-term outcomes, and future of SDR. We aim to help clinicians and caregivers of children with spastic CP with their management plan.</p>

