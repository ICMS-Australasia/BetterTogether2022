 <h3 class="teal">Full Day Pre-Conference Sessions</h3>

 <h5 class="teal mt-4"><em>All full day pre-conference sessions will run from 9:00am – 5:30pm (AEDT) and are available for both in-person and virtual registration.</em></h5>

 <p><b>Improving the health and wellbeing of adults with cerebral palsy </b></p>
 <p><em>Mark Peterson, Edward Hurvitz, Jan Willem Gorter, Elisabet Rodby-Bousquet, Sarah Reedman, Dinah Reddihough, Sevastine Katsakis, David Coghill, Prue Morgan, Rob Carter</em></p>
 <p>This workshop will include international experts discussing how we can meet the medical, rehabilitation and participatory needs of adults with cerebral palsy. This will include the role of physical and mental health and prevalence and risk factors for cardiovascular and musculoskeletal disease. We will integrate knowledge translation: embedding the priorities of adults living with cerebral palsy with a focus on building supportive health and disability service environments and
incorporating health economics into research to drive policy and practice.</p>

<hr>

 <p><b>Muscle in cerebral palsy</b></p>
 <p><em>Richard Leiber, Sudarshan Dayanidhi, Tandy Hastings-Ison</em></p>
 <p>This workshop across seven different sessions will focus on structural, functional, and adaptive changes in muscle for people who live with cerebral palsy. Multi-disciplinary speakers will address a range of topics affecting muscle including Botulinum toxin, from basic science research to clinical management, with audience participation regarding optimal muscle health</p>

<hr>

 <p><b>Early Intervention for infants with cerebral palsy or born preterm</b></p>
 <p><em>Cathy Morgan, Iona Novak, Ann Christian Eliasson, Stacey Dusing, Ros Boyd, Alicia Spittle, Laura Prosser, Regina Harbourne </em></p>
 <p>This workshop will outline the current state of the science in 2022 on early intervention for cerebral palsy. This pre-conference symposium will include a multidisciplinary global panel of experts and present the key ingredients and results of novel early intervention clinical trials. The day will include discussion on implementation strategies, statistical challenges and future research opportunities.</p>

