 <h5 class="teal mt-4"><em>This pre-conference session will run from 10:30am – 12:00pm (AEDT) and is available for both in-person and virtual registration.</em></h5>
 <p><b>Cultural influences on early screening and intervention for Aboriginal and Torres Strait Islander infants at risk of Neurodevelopmental Disabilities</b></p>
 <p><em>Kath Benfer, Marjad Page, Dianne Shanley, Leann Ramsamy, Carly Luke</em></p>
 <p>Little is known about the Aboriginal and Torres Strait Islander construct of disability. This understanding has significant implications for how First Nations communities engage with child development and disability services, including screening and ‘intervention’ programs. With greater awareness of the importance of earlier screening and intervention based on the International Clinical Practice Guideline (Novak 2017), combined with increasing availability of disability services under NDIS, it is pertinent to focus on the role of culture in such programs.</p>

