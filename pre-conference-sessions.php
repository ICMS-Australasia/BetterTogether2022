 <h5 class="teal mt-4"><em>This pre-conference session will run from 8:30am – 10:00am (AEDT) and is available for both in-person and virtual registration.</em></h5>
 <p><b>Exploring inequities in service provision for Indigenous and First Nations women, their babies and children with cerebral palsy</b></p>
 <p><em>Alexandra Sorhage, Danny DeLore, Tan Martin, Juanita Sherwood, Michael Shevell, Anna Mackey, Sian Williams, Hayley Smithers-Sheedy, Sarah McIntyre, Sue Woolfenden</em></p>
 <p>This symposium will identify success stories and areas of continuing inequity for First Nations’ people with cerebral palsy (CP). Participants will be encouraged to discuss and reflect on reasons for and approaches to addressing potential inequity within their own work, and will leave with ideas to apply within their own context.</p>

