<h2>Networking</h2>

<p>Although the Conference has moved fully virtual, the organisers still encourage you to participate in the networking elements to get the most out of the social aspect of the event. </p>

<h3>Names, Games & Rock’n’Roll</h3>
<p>To assist in accommodating all timezones, the organisers are planning two exciting Welcome Receptions. These functions are going to be high-energy and a lot of fun and everyone is encouraged to attend at least one. </p>

<p>Names, Games & Rock n Roll is an engaging and fun-filled session that combines your love of music, games & general knowledge in a laugh out loud, virtual event. There is something for everyone to enjoy in these interactive sessions including trivia, live music performed by Brihony and their talented team and plenty of themed games to give attendees back their spark, learn hidden skills and connect like you never have before.</p>

