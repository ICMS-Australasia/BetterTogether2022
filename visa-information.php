<h2>Deadlines</h2>
<p>We recommend that all delegates apply for their Australian visa a <b>minimum of 2 months prior</b> to their arrival in the country.</p>

<h2>Visa Options for Conferences</h2>
 <p>All visitors to Australia must have a valid visa to travel to and enter Australia (other than New Zealand passport holders who will normally be granted a Special Category visa on arrival, provided they meet health and character requirements; and permanent residents of Norfolk Island who may be granted a Permanent Resident of Norfolk Island visa on arrival).</p>
 <p>The appropriate visa will depend, amongst other things, on your purpose for visiting Australia.</p>
 <p>The most common Visa’s that may be recommended to you for short travel to Australia to attend a conference are;</p>
<h3>1. ETA (601)</h3>
<ul>
    <li>Online Application Process</li>
    <li>Quick</li>
</ul>
 <p>Eligibility for an eVisitor visa and ETA will be determined by the country of passport that the applicant holds. The ETA is a quick, online, charge free process, but incurs a AUD$20 service provider charge, and/or a further charge if applied for through a travel agency, airline agency or specialist service provider. Further information on the ETA can be found on the <a href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-listing/electronic-travel-authority-601" target="_blank">website</a>.</p>
<h3>2. eVisitor (651)</h3>
<ul>
    <li>Online Application Process</li>
    <li>Quick</li>
    <li>One working day processing time</li>
    <li>No Application charge or service fee</li>
</ul>
 <p>Eligibility for an eVisitor visa and ETA will be determined by the country of passport that the applicant holds. The eVisitor visa is an online visa process, with a one working day processing time for low risk applications. There is no visa application charge or service fee. Further information on the eVisitor visa, including how to apply online, can be found on the <a href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-listing/evisitor-651" target="_blank">website</a>.</p>
<h3>3. Visitor Visa (subclass 600) in the Business Visitor stream</h3>
<ul>
    <li>Online or Paper application Process</li>
</ul>
<p>Passport holders who are not eligible to apply for an eVisitor or an ETA should apply for a Visitor Visa (subclass 600) in the Business Visitor Stream.</p>
<ul>
    <li>Online application process</li>
    <li>Application fee of $140 AUD</li>
</ul>
<p>Further information on the Subclass 600 Visa can be found on the <a href="https://immi.homeaffairs.gov.au/visas/getting-a-visa/visa-listing/visitor-600/business-visitor-stream" target="_blank">website</a>.</p>

<h2>Invitation and registration to attend the event.</h2>
 <p>Although this is a separate process to lodging your visa application, when assessing whether you meet the criteria for the grant of a visa, the department may take into account whether you are registered or were invited to attend the event.</p>
 <p>Confirmation of registration, or a copy of your letter of invitation, should accompany visa applications. For online subclass 400 and subclass 600 applications, information about documents that need to be attached will be provided as part of the application process. Your letter of invitation will be sent to you with your registration successful letter.</p>

<h2>Things to consider before lodging an application</h2>
<h3>Health and Character Requirements</h3>
 <p>All visa applicants must be assessed against Australia’s health and character requirements. These requirements are designed to protect the safety and security of the Australian community.</p>
<h4>Health requirements</h4>
<p>As part of the visa application process you may be required to undertake a medical examination, chest x ray and/or other health checks.</p>
<p>The Department of Immigration and Border Protection (the department) will advise you of the applicable health checks (if any), based on your individual circumstances.</p>
<p>Information on which health checks may apply to you is available on the department’s website.</p>
<p>See: <a href="http://www.border.gov.au/Trav/Visa/Heal" target="_blank">www.border.gov.au/Trav/Visa/Heal</a></p>
<h4>Character requirements</h4>
<p>As part of the visa application assessment process the onus is on you to satisfy the character requirements set out in Section 501 of the Migration Act 1958. Further information on the character requirement is available on the department’s website. See: www.border.gov.au/Trav/Visa/Char</p>
<p>If you are unsure whether you satisfy the character requirements because of prior criminal convictions, you should lodge a paper-based application at the Australian visa office nearest to your current place of residence, at least four months prior to the event start date.</p>
<p>Contact information for Australian visa offices is available on the department’s website.</p>
<p>See: <a href="http://www.border.gov.au/about/contact/offices-locations" target="_blank">www.border.gov.au/about/contact/offices-locations</a></p>
<h3>Travel sanctions</h3>
<p>For information on countries subject to travel sanctions, please refer to the Department of Foreign Affairs and Trade’s website. Nationals of these countries may not be permitted to travel to and enter Australia.</p>
<p>See: <a href="http://www.dfat.gov.au/international-relations/security/sanctions/pages/sanctions.aspx" target="_blank">www.dfat.gov.au/international-relations/security/sanctions/pages/sanctions.aspx</a></p>
 <h3>Biometrics programme</h3>
 <p>Biometrics may need to be collected at an Australian Visa Application Centre for visitors and business people lodging a visa application from the countries listed on the department’s website.</p>
 <p>See: <a href="http://www.border.gov.au/Trav/Visa/Biom" target="_blank">www.border.gov.au/Trav/Visa/Biom</a></p>
 <p>If you lodge your application online, the department will advise you in writing if you need to provide your biometrics.</p>

