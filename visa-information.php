<h2>Obligations of visa holders</h2>
<p>On arrival at Australia’s border you must identify yourself, present your travel documents, and complete an Incoming Passenger Card (IPC). The IPC requires visitors to declare their health status and any prior criminal convictions.</p>
<p>If you do not meet immigration clearance requirements, you may be refused entry to Australia.</p>
<p>More information on immigration clearance processes at Australia’s borders is available on the department’s website.</p>
<p>See: <a href="http://www.border.gov.au/Busi/Comp">www.border.gov.au/Busi/Comp</a></p>
<p>You must comply with your visa validity and visa conditions while in Australia. When you receive your Australian visa you are issued with a visa grant notice that explains the conditions of the visa including period of validity and entry requirements. You are advised to carry this visa grant notice when travelling for your own reference.</p>
<p>Your visa may be cancelled if you breach any of the conditions.</p>
<p>If your visa expires while you are still in Australia, you become an unlawful non-citizen. Unlawful non-citizens are liable to be detained and removed from Australia.</p>



